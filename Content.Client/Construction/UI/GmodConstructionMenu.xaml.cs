    /// <summary>The spawnlist currently selected in the left tree.</summary>
    public string SelectedSpawnlist => _selectedSpawnlist;

    /// <summary>Rebuilds the left-tree spawnlist buttons from the presenter's discovered spawnlists.</summary>
    public void SetSpawnlists(IReadOnlyList<string> spawnlists)
    {
        _allSpawnlists = spawnlists;

        // Keep a valid selection for the Spawnlists page (never the Tiles-only entry).
        if (_selectedSpawnlist == TilesSpawnlist || !spawnlists.Contains(_selectedSpawnlist))
            _selectedSpawnlist = spawnlists.FirstOrDefault(s => s != TilesSpawnlist) ?? DefaultSpawnlist;

        BuildTree();

        // Reflect the current selection in the buttons without re-firing population.
        if (_currentPage == MenuPage.Tiles)
        {
            if (_spawnlistButtons.TryGetValue(TilesSpawnlist, out var tilesButton))
                tilesButton.Pressed = true;
            ExpandCategoriesFor(TilesSpawnlist);
        }
        else if (_currentTreeCategory == FavoriteCatName || _savedBuildsActive)
        {
            CollapseAllCategories();
        }
        else
        {
            foreach (var (name, button) in _spawnlistButtons)
                button.Pressed = name == _selectedSpawnlist;
            // Setting Button.Pressed never fires OnToggled, so expand the selection's categories explicitly.
            ExpandCategoriesFor(_selectedSpawnlist);
        }

        RecolorKeyLabels(TreePanel);
    }

    /// <summary>
    /// Rebuilds the left-tree contents for the current page: the Spawnlists page lists every spawnlist except
    /// Tiles (plus the synthetic Saved Builds entry and Favorites), while the Tiles page lists only Tiles.
    /// </summary>
    private void BuildTree()
    {
        SpawnlistContainer.RemoveAllChildren();
        _spawnlistButtons.Clear();
        _categoryContainers.Clear();
        _categoryButtons.Clear();
        _spawnlistArrowSetters.Clear();
        _expandedSpawnlist = null;
        _savedBuildsButton = null;

        if (_currentPage == MenuPage.Tiles)
        {
            // The "Z-Level (Experimental)" page gathers the Tiles and ZLevel spawnlists together.
            foreach (var spawnlist in _allSpawnlists)
            {
                if (!IsZLevelPageSpawnlist(spawnlist))
                    continue;

                AddSpawnlistRow(spawnlist);
            }

            // Favorites and Saved Builds belong to the Spawnlists page only.
            TreeFavorites.Visible = false;
            return;
        }

        foreach (var spawnlist in _allSpawnlists)
        {
            if (IsZLevelPageSpawnlist(spawnlist))
                continue;

            AddSpawnlistRow(spawnlist);
        }

        // Append the synthetic "Saved Builds" spawnlist (player-saved builds, not recipes).
        _savedBuildsButton = MakeSavedBuildsButton();
        SpawnlistContainer.AddChild(_savedBuildsButton);

        TreeFavorites.Visible = Array.IndexOf(_categories, FavoriteCatName) >= 0;
    }

    /// <summary>Flips between the Spawnlists and Tiles pages, rebuilding the tree and selecting a default.</summary>
    private void SelectPage(MenuPage page)
    {
        _currentPage = page;
        TabSpawnlists.Pressed = page == MenuPage.Spawnlists;
        TabTiles.Pressed = page == MenuPage.Tiles;

        _currentTreeCategory = ForAllCategoryName;
        TreeFavorites.Pressed = false;
        DeactivateSavedBuilds();

        BuildTree();
        RecolorKeyLabels(TreePanel);

        string target;
        if (page == MenuPage.Tiles)
        {
            // Prefer the ZLevel spawnlist on this page, falling back to Tiles or whatever is present.
            target = _spawnlistButtons.ContainsKey("ZLevel")
                ? "ZLevel"
                : (_spawnlistButtons.Keys.FirstOrDefault() ?? TilesSpawnlist);
        }
        else
        {
            target = _spawnlistButtons.ContainsKey(_selectedSpawnlist)
                ? _selectedSpawnlist
                : (_spawnlistButtons.Keys.FirstOrDefault() ?? DefaultSpawnlist);
        }

        _selectedSpawnlist = target;
        foreach (var (name, button) in _spawnlistButtons)
            button.Pressed = name == target;

        // Setting Button.Pressed never fires OnToggled, so expand the new selection's categories explicitly.
        ExpandCategoriesFor(target);

        PopulateRecipes?.Invoke(this, (SearchBar.Text, ForAllCategoryName));
    }

    private BaseButton MakeSpawnlistButton(string spawnlist)
    {
        // Only spawnlists that actually have categories get the collapse arrow (nothing to expand otherwise).
        var collapsible = CategoriesForSpawnlist(spawnlist).Count > 0;

        BaseButton button;
        if (spawnlist == DefaultSpawnlist && _logoTexture != null)
        {
            // AU14 gets the emblem next to its label. A ContainerButton has no stylesheet background of
            // its own (unlike a plain Button), so wrap the content in a PanelContainer to give it the same
            // themed, amber-framed background as the other spawnlist entries.
            var containerButton = new ContainerButton
            {
                ToggleMode = true,
                HorizontalExpand = true,
                Margin = new Thickness(0, 0, 0, 2),
            };

            var row = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                Margin = new Thickness(6, 4, 6, 4),
            };

            if (collapsible)
            {
                var arrow = new Label { Text = ArrowCollapsed, VerticalAlignment = Control.VAlignment.Center, Margin = new Thickness(0, 0, 4, 0) };
                row.AddChild(arrow);
                _spawnlistArrowSetters[spawnlist] = expanded => arrow.Text = expanded ? ArrowExpanded : ArrowCollapsed;
            }

            // Greyscale (desaturated) AU14 emblem - see GreyscaleTextureRect.
            row.AddChild(new GreyscaleTextureRect
            {
                Texture = _logoTexture,
                SetSize = new Vector2(18, 18),
                Margin = new Thickness(0, 0, 6, 0),
                VerticalAlignment = Control.VAlignment.Center,
            });
            row.AddChild(new Label { Text = spawnlist, VerticalAlignment = Control.VAlignment.Center });

            // Use the shared spawnlist color so the AU14 entry matches the other spawnlist buttons, and swap to
            // the selected tint on toggle (a ContainerButton has no stylesheet background of its own).
            var panel = new PanelContainer { PanelOverride = Panel(SpawnlistBg), Children = { row } };
            containerButton.AddChild(panel);
            containerButton.OnToggled += _ =>
                panel.PanelOverride = Panel(containerButton.Pressed ? SpawnlistSelected : SpawnlistBg);
            button = containerButton;
        }
        else
        {
            var plain = new Button
            {
                // The arrow prefixes the label; the setter rewrites just the glyph on expand/collapse.
                Text = collapsible ? $"{ArrowCollapsed} {spawnlist}" : spawnlist,
                ToggleMode = true,
                HorizontalExpand = true,
                Margin = new Thickness(0, 0, 0, 2),
            };
            // Shared spawnlist color (distinct from the neutral Saved Builds / Favorites buttons).
            Modernize(plain, toggle: true, SpawnlistBg, SpawnlistSelected);
            if (collapsible)
                _spawnlistArrowSetters[spawnlist] = expanded => plain.Text = $"{(expanded ? ArrowExpanded : ArrowCollapsed)} {spawnlist}";
            button = plain;
        }

        button.OnToggled += args =>
        {
            if (!args.Pressed)
            {
                // Re-clicking the active spawnlist collapses (or re-expands) its category list instead of
                // deselecting it - the spawnlist stays selected and its recipes stay shown.
                if (_selectedSpawnlist == spawnlist && _currentTreeCategory != FavoriteCatName)
                {
                    button.Pressed = true;
                    if (_expandedSpawnlist == spawnlist)
                        CollapseAllCategories();
                    else
                        ExpandCategoriesFor(spawnlist);
                }
                return;
            }

            _selectedSpawnlist = spawnlist;
            _currentTreeCategory = ForAllCategoryName;

            foreach (var (name, other) in _spawnlistButtons)
            {
                if (name != spawnlist)
                    other.Pressed = false;
            }
            TreeFavorites.Pressed = false;
            DeactivateSavedBuilds();

            // Expand this spawnlist's categories beneath it (and collapse the previously-open one).
            ExpandCategoriesFor(spawnlist);

            PopulateRecipes?.Invoke(this, (SearchBar.Text, ForAllCategoryName));
        };

        return button;
    }

    /// <summary>Adds one spawnlist button plus its (initially empty and hidden) category sub-list to the tree.</summary>
    private void AddSpawnlistRow(string spawnlist)
    {
        var button = MakeSpawnlistButton(spawnlist);
        _spawnlistButtons[spawnlist] = button;
        SpawnlistContainer.AddChild(button);

        var categoryBox = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Vertical, Visible = false };
        _categoryContainers[spawnlist] = categoryBox;
        SpawnlistContainer.AddChild(categoryBox);
    }

    /// <summary>
    /// Expands the given spawnlist's categories beneath its button and collapses every other spawnlist's list.
    /// Called on both a user click and the programmatic selection paths (setting Button.Pressed does NOT fire
    /// OnToggled), so the categories stay in sync with the selected spawnlist however it was chosen.
    /// </summary>
    private void ExpandCategoriesFor(string spawnlist)
    {
        _categoryButtons.Clear();
        foreach (var (name, box) in _categoryContainers)
        {
            if (name == spawnlist)
                continue;

            box.Visible = false;
            box.RemoveAllChildren();
        }

        // Every other spawnlist's arrow points collapsed.
        foreach (var (name, setArrow) in _spawnlistArrowSetters)
        {
            if (name != spawnlist)
                setArrow(false);
        }

        if (!_categoryContainers.TryGetValue(spawnlist, out var container))
        {
            _expandedSpawnlist = null;
            return;
        }

        container.RemoveAllChildren();
        BuildCategoryButtons(spawnlist, container);

        var expanded = container.ChildCount > 0;
        container.Visible = expanded;
        _expandedSpawnlist = expanded ? spawnlist : null;
        if (_spawnlistArrowSetters.TryGetValue(spawnlist, out var setThis))
            setThis(expanded);

        RecolorKeyLabels(TreePanel);
    }

    /// <summary>Collapses every spawnlist's category sub-list (re-clicking the active spawnlist, or selecting
    /// Favorites / Saved Builds). Points every arrow back to collapsed.</summary>
    private void CollapseAllCategories()
    {
        _categoryButtons.Clear();
        _expandedSpawnlist = null;
        foreach (var box in _categoryContainers.Values)
        {
            box.Visible = false;
            box.RemoveAllChildren();
        }

        foreach (var setArrow in _spawnlistArrowSetters.Values)
            setArrow(false);
    }

    /// <summary>
    /// Fills a spawnlist's category sub-list: an "All" entry (selected by default, mirrors the spawnlist's own
    /// unfiltered view) followed by one button per category actually present in that spawnlist. A spawnlist with
    /// no categorized recipes gets no buttons, so the tree stays compact.
    /// </summary>
    private void BuildCategoryButtons(string spawnlist, BoxContainer container)
    {
        var categories = CategoriesForSpawnlist(spawnlist);
        if (categories.Count == 0)
            return;

        var allButton = MakeCategoryButton(Loc.GetString(ForAllCategoryName), ForAllCategoryName);
        allButton.Pressed = _currentTreeCategory == ForAllCategoryName;
        _categoryButtons[ForAllCategoryName] = allButton;
        container.AddChild(allButton);

        foreach (var (raw, display) in categories)
        {
            var button = MakeCategoryButton(display, raw);
            button.Pressed = _currentTreeCategory == raw;
            _categoryButtons[raw] = button;
            container.AddChild(button);
        }
    }

    private BaseButton MakeCategoryButton(string label, string categoryValue)
    {
        var button = new Button
        {
            Text = label,
            ToggleMode = true,
            HorizontalExpand = true,
            // Indented under the parent spawnlist so it reads as a child level.
            Margin = new Thickness(14, 0, 0, 2),
        };
        Modernize(button, toggle: true, CategoryBg, CategorySelected);
        // Smaller than the spawnlist label so the nesting reads at a glance.
        if (_categoryFont != null)
            button.Label.FontOverride = _categoryFont;

        button.OnToggled += args =>
        {
            if (!args.Pressed)
            {
                // Keep a selection at all times: re-clicking the active category is a no-op.
                if (_currentTreeCategory == categoryValue)
                    button.Pressed = true;
                return;
            }

            _currentTreeCategory = categoryValue;
            foreach (var (value, other) in _categoryButtons)
            {
                if (value != categoryValue)
                    other.Pressed = false;
            }

            PopulateRecipes?.Invoke(this, (SearchBar.Text, categoryValue));
        };

        return button;
    }

    /// <summary>
    /// The distinct categories present in a spawnlist, as (raw category value, localized display name) ordered
    /// by display name. Mirrors the presenter's spawnlist filter: the default "AU14" list spans every non
    /// z-level-page spawnlist (it is the "All" view), while any other spawnlist shows only its own recipes.
    /// </summary>
    private List<(string Raw, string Display)> CategoriesForSpawnlist(string spawnlist)
    {
        var raw = new HashSet<string>();
        foreach (var recipe in _prototypeManager.EnumerateCM<ConstructionPrototype>())
        {
            if (recipe.Hide || string.IsNullOrEmpty(recipe.Category))
                continue;

            var recipeSpawnlist = string.IsNullOrEmpty(recipe.Spawnlist) ? DefaultSpawnlist : recipe.Spawnlist;
            if (spawnlist == DefaultSpawnlist)
            {
                // The AU14 "All" view excludes the separate Z-Level page's spawnlists (see GetAndSortRecipes).
                if (IsZLevelPageSpawnlist(recipeSpawnlist))
                    continue;
            }
            else if (recipeSpawnlist != spawnlist)
            {
                continue;
            }

            raw.Add(recipe.Category);
        }

        return raw
            .Select(c => (Raw: c, Display: Loc.GetString(c)))
            .OrderBy(c => c.Display, StringComparer.InvariantCulture)
            .ToList();
    }

