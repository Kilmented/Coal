using Content.Client.Message;
using Content.Shared._AU14.Callsigns;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;

namespace Content.Client._AU14.Callsigns;

[GenerateTypedNameReferences]
public sealed partial class AU14CallsignConsoleWindow : DefaultWindow
{
    public event Action<NetEntity?, string?, string>? OnRenameElement;
    public event Action<NetEntity, string>? OnSetSuffix;
    public event Action<string>? OnCreateGroup;
    public event Action<string>? OnDeleteGroup;
    public event Action<NetEntity, string?>? OnAssignGroup;

    private static readonly Color Accent = Color.FromHex("#6BC7FF");
    private static readonly Color TextBright = Color.FromHex("#C7D7EA");
    private static readonly Color TextDim = Color.FromHex("#9DB5D1");
    private static readonly Color PanelBg = Color.FromHex("#0B131F");
    private static readonly Color PanelBorder = Color.FromHex("#1E3450");

    private bool _canEdit;
    private List<string> _groups = new();

    public AU14CallsignConsoleWindow()
    {
        RobustXamlLoader.Load(this);

        Title = Loc.GetString("au14-callsign-console-title");

        FooterLabel.SetMarkupPermissive(
            $"[color=#9DB5D1]{Loc.GetString("au14-callsign-console-footer")}[/color]");

        GroupCreateButton.OnPressed += _ =>
        {
            var word = GroupWordEdit.Text.Trim();

            if (word.Length == 0)
                return;

            OnCreateGroup?.Invoke(word);
            GroupWordEdit.Text = string.Empty;
        };
    }

    public void UpdateState(AU14CallsignConsoleState state, bool canEdit)
    {
        _canEdit = canEdit;
        _groups = state.Groups;

        GroupCreateRow.Visible = canEdit;

        FactionLabel.Text = Loc.GetString("au14-callsign-console-net", ("faction", state.Faction.ToUpperInvariant()));
        EditModeLabel.Text = canEdit
            ? Loc.GetString("au14-callsign-console-mode-edit")
            : Loc.GetString("au14-callsign-console-mode-readonly");
        EditModeLabel.FontColorOverride = canEdit ? Accent : TextDim;

        var stations = 0;

        ElementList.RemoveAllChildren();

        foreach (var element in state.Elements)
        {
            stations += element.Rows.Count;
            ElementList.AddChild(BuildElementPanel(element));
        }

        EntryCountLabel.Text = Loc.GetString("au14-callsign-console-stations", ("count", stations));
    }

    private Control BuildElementPanel(AU14CallsignConsoleElement element)
    {
        var panel = new PanelContainer
        {
            HorizontalExpand = true,
            PanelOverride = new StyleBoxFlat
            {
                BackgroundColor = PanelBg,
                BorderColor = PanelBorder,
                BorderThickness = new Thickness(1),
            },
        };

        var body = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            Margin = new Thickness(10, 8),
            SeparationOverride = 4,
            HorizontalExpand = true,
        };

        // header: element word + label, plus a rename control for editors
        var header = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            SeparationOverride = 8,
            HorizontalExpand = true,
        };

        header.AddChild(new Label
        {
            Text = element.Word,
            FontColorOverride = Accent,
            MinWidth = 120,
        });

        header.AddChild(new Label
        {
            Text = element.Label,
            FontColorOverride = TextDim,
            HorizontalExpand = true,
        });

        // command, squads and role sections carry renamable element words
        if (_canEdit && element.Group == null)
        {
            var renameEdit = new LineEdit
            {
                PlaceHolder = Loc.GetString("au14-callsign-console-rename-placeholder"),
                MinWidth = 120,
            };

            var renameButton = new Button
            {
                Text = Loc.GetString("au14-callsign-console-set-button"),
                MinWidth = 48,
            };

            var squad = element.Squad;
            var category = element.Category;

            renameButton.OnPressed += _ =>
            {
                var word = renameEdit.Text.Trim();

                if (word.Length == 0)
                    return;

                OnRenameElement?.Invoke(squad, category, word);
                renameEdit.Text = string.Empty;
            };

            header.AddChild(renameEdit);
            header.AddChild(renameButton);
        }

        // custom groups are identified by their word; they get disbanded, not renamed
        if (_canEdit && element.Group is { } group)
        {
            var deleteButton = new Button
            {
                Text = Loc.GetString("au14-callsign-console-delete-group-button"),
                MinWidth = 64,
            };

            deleteButton.OnPressed += _ => OnDeleteGroup?.Invoke(group);

            header.AddChild(deleteButton);
        }

        body.AddChild(header);

        if (element.Rows.Count == 0)
        {
            body.AddChild(new Label
            {
                Text = Loc.GetString("au14-callsign-console-no-stations"),
                FontColorOverride = TextDim,
            });
        }

        foreach (var row in element.Rows)
        {
            body.AddChild(BuildMemberRow(row, element.Group));
        }

        panel.AddChild(body);
        return panel;
    }

    private Control BuildMemberRow(AU14CallsignConsoleRow row, string? currentGroup)
    {
        var line = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            SeparationOverride = 8,
            HorizontalExpand = true,
        };

        line.AddChild(new Label
        {
            Text = row.Callsign,
            FontColorOverride = Accent,
            MinWidth = 140,
            ClipText = true,
        });

        line.AddChild(new Label
        {
            Text = row.Name,
            FontColorOverride = TextBright,
            HorizontalExpand = true,
            ClipText = true,
        });

        line.AddChild(new Label
        {
            Text = row.Job,
            FontColorOverride = TextDim,
            MinWidth = 140,
            ClipText = true,
        });

        if (_canEdit)
        {
            var suffixEdit = new LineEdit
            {
                PlaceHolder = Loc.GetString("au14-callsign-console-suffix-placeholder"),
                MinWidth = 70,
            };

            var suffixButton = new Button
            {
                Text = Loc.GetString("au14-callsign-console-set-button"),
                MinWidth = 48,
            };

            var member = row.Member;

            suffixButton.OnPressed += _ =>
            {
                var suffix = suffixEdit.Text.Trim();

                if (suffix.Length == 0)
                    return;

                OnSetSuffix?.Invoke(member, suffix);
                suffixEdit.Text = string.Empty;
            };

            line.AddChild(suffixEdit);
            line.AddChild(suffixButton);

            if (_groups.Count > 0)
                line.AddChild(BuildGroupSelector(member, currentGroup));
        }

        return line;
    }

    private Control BuildGroupSelector(NetEntity member, string? currentGroup)
    {
        var selector = new OptionButton
        {
            MinWidth = 90,
        };

        selector.AddItem(Loc.GetString("au14-callsign-console-group-none"), 0);

        for (var i = 0; i < _groups.Count; i++)
        {
            selector.AddItem(_groups[i], i + 1);

            if (string.Equals(_groups[i], currentGroup, StringComparison.OrdinalIgnoreCase))
                selector.SelectId(i + 1);
        }

        selector.OnItemSelected += args =>
        {
            selector.SelectId(args.Id);

            var group = args.Id == 0 ? null : _groups[args.Id - 1];
            OnAssignGroup?.Invoke(member, group);
        };

        return selector;
    }
}
