    private static readonly Color LcdBright = Color.FromHex("#3FCF8E");
    private static readonly Color LcdMid = Color.FromHex("#2D9E60");
    private static readonly Color LcdDim = Color.FromHex("#4A7A55");
    private static readonly Color LcdOff = Color.FromHex("#1E4A2E");
    private static readonly Color LcdWarning = Color.FromHex("#D6B85A");

    private static readonly Color TextBright = Color.FromHex("#C8D2E8");
    private static readonly Color TextDim = Color.FromHex("#687A9A");
    private static readonly Color TextGood = Color.FromHex("#78D69B");
    private static readonly Color TextWarn = Color.FromHex("#D6B85A");
    private static readonly Color TextBad = Color.FromHex("#D65A5A");
